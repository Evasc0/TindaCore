export interface StarterProduct {
  id: string;
  name: string;
  category: string;
  default_unit: string;
}

type StarterSeed = {
  name: string;
  defaultUnit?: string;
};

const combine = (prefixes: string[], suffixes: string[], separator = " ") =>
  prefixes.flatMap(prefix => suffixes.map(suffix => `${prefix}${separator}${suffix}`.trim()));

const toSeeds = (names: string[], defaultUnit?: string): StarterSeed[] =>
  names.map(name => ({ name, defaultUnit }));

const buildCategory = (category: string, seeds: StarterSeed[], fallbackUnit: string): StarterProduct[] =>
  seeds.map(seed => ({
    id: "",
    name: seed.name,
    category,
    default_unit: seed.defaultUnit || fallbackUnit,
  }));

